public class ComparisonOperators {
    public static void main(String[] args) {
        // Java pProgram Demonstrating different comparison operators such as ==, !=, >, <, >=, <=
        int a = 10;
        int b = 5;

        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));
        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a >= b : " + (a >= b));
        System.out.println("a <= b : " + (a <= b));
    }
}
